                      fontFamily: 'Lexend Deca',
                      color: Colors.white,
                      fontSize: 20,
                      fontWeight: FontWeight.normal,
                    ),
              ).animated([animationsMap['textOnPageLoadAnimation2']!]),
            ),
            FFButtonWidget(
              onPressed: () async {
                logFirebaseEvent('ORDER_SUCCESS_PAGE_ORDERS_BTN_ON_TAP');
